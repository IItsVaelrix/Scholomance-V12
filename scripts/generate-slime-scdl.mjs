#!/usr/bin/env node
/**
 * generate-slime-scdl.mjs
 *
 * Emits a slime SCDL fixture from proper sphere math.
 *
 *   Shape:   d² = (x - cx)² + (y - cy)²
 *   Shading: cosθ = n · L
 *     n  = (dx, dy) / r                                  ← outward normal
 *     L  = (lx, ly) / |L|                                ← toward light source
 *
 *   Color tiers from cosθ:
 *     cosθ ≥  0.999 → shine  (1-3 px specular dot)
 *     cosθ ≥  0.70  → glow   (tight lit halo)
 *     cosθ ≥  0.05  → core   (main body)
 *     cosθ ≥ -0.40  → rim    (terminator)
 *     cosθ < -0.40  → shadow (dark side)
 *
 * Usage:
 *   node generate-slime-scdl.mjs                          # default WILL/TRANSCENDENT slime
 *   node generate-slime-scdl.mjs --h 0 --s 78 --l 48 \\
 *     --out codex/core/pixelbrain/scdl/fixtures/crimson-ooze.scdl \\
 *     --name crimson_ooze
 */

import { writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

function parseArgs(argv) {
  const opts = { h: 356, s: 83, l: 52, out: null, name: 'slime' };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--h')     opts.h = Number(argv[++i]);
    else if (a === '--s') opts.s = Number(argv[++i]);
    else if (a === '--l') opts.l = Number(argv[++i]);
    else if (a === '--out') opts.out = argv[++i];
    else if (a === '--name') opts.name = argv[++i];
  }
  return opts;
}

const opts = parseArgs(process.argv.slice(2));

const canvas = 24;
const cx = (canvas - 1) / 2;
const cy = cx;
const r = 10;
const r2 = r * r;

const lx = -1;
const ly = -1;
const lLen = Math.hypot(lx, ly);
const lNormX = lx / lLen;
const lNormY = ly / lLen;

function hslToHex(h, s, l) {
  const sN = s / 100;
  const lN = l / 100;
  const c = (1 - Math.abs(2 * lN - 1)) * sN;
  const hp = h / 60;
  const x = c * (1 - Math.abs((hp % 2) - 1));
  let r1, g1, b1;
  if      (hp < 1) [r1, g1, b1] = [c, x, 0];
  else if (hp < 2) [r1, g1, b1] = [x, c, 0];
  else if (hp < 3) [r1, g1, b1] = [0, c, x];
  else if (hp < 4) [r1, g1, b1] = [0, x, c];
  else if (hp < 5) [r1, g1, b1] = [x, 0, c];
  else             [r1, g1, b1] = [c, 0, x];
  const m = lN - c / 2;
  const toHex = (v) => Math.round((v + m) * 255).toString(16).padStart(2, '0');
  return `#${toHex(r1)}${toHex(g1)}${toHex(b1)}`.toUpperCase();
}

const hexCore   = hslToHex(opts.h, opts.s, opts.l);
const hexRim    = hslToHex(opts.h, Math.max(0, opts.s - 13), Math.max(5, opts.l - 24));
const hexGlow   = hslToHex(opts.h, Math.min(100, opts.s + 12), Math.min(95, opts.l + 23));
const hexShadow = hslToHex(opts.h, Math.max(0, opts.s - 23), Math.max(3, opts.l - 40));
const hexShine  = hslToHex(opts.h, Math.min(100, opts.s + 12), Math.min(98, opts.l + 30));

function tierFor(cosTheta) {
  if (cosTheta >= 0.999) return 'shine';
  if (cosTheta >= 0.70)  return 'glow';
  if (cosTheta >= 0.05)  return 'core';
  if (cosTheta >= -0.40) return 'rim';
  return 'shadow';
}

const bodyCells = [];
for (let y = 0; y < canvas; y++) {
  for (let x = 0; x < canvas; x++) {
    const dx = x - cx;
    const dy = y - cy;
    const d2 = dx * dx + dy * dy;
    if (d2 > r2) continue;
    const d  = Math.sqrt(d2);
    const nx = dx / d;
    const ny = dy / d;
    const cosTheta = nx * lNormX + ny * lNormY;
    bodyCells.push({ x, y, color: tierFor(cosTheta) });
  }
}

const byRow = new Map();
for (const c of bodyCells) {
  if (!byRow.has(c.y)) byRow.set(c.y, []);
  byRow.get(c.y).push(c);
}

const bodyLines = [];
for (const y of [...byRow.keys()].sort((a, b) => a - b)) {
  const row = byRow.get(y).sort((a, b) => a.x - b.x);
  for (const c of row) {
    bodyLines.push(`  cell ${c.x} ${c.y} ${c.color}`);
  }
}

const highlightCells = [{ x: 7, y: 7, color: 'shine' }];
const highlightLines = highlightCells.map(c => `  cell ${c.x} ${c.y} ${c.color}`);

const source = [
  `# ${opts.name}.scdl`,
  '# Generated by scripts/generate-slime-scdl.mjs',
  `# Palette derived from hsl(${opts.h}, ${opts.s}%, ${opts.l}%)`,
  '# Shape:   d² = (x - cx)² + (y - cy)²,   cx = cy = (canvas-1)/2',
  '# Shade:   cosθ = n · L,   n = (dx,dy)/r,   L = (-1,-1)/√2 (upper-left light)',
  `# Canvas ${canvas}x${canvas}, radius r=${r}, ${bodyCells.length} lit cells`,
  '',
  `asset ${opts.name} canvas 24x24`,
  '',
  'palette {',
  `  core   = ${hexCore}`,
  `  rim    = ${hexRim}`,
  `  glow   = ${hexGlow}`,
  `  shine  = ${hexShine}`,
  `  shadow = ${hexShadow}`,
  '}',
  '',
  `part body material ${opts.name}_material {`,
  ...bodyLines,
  '  glow radius 2',
  '}',
  '',
  'part highlight material emissive {',
  ...highlightLines,
  '}',
  '',
  'export json svg phaser png',
  '',
].join('\n');

const outPath = opts.out
  ? resolve(opts.out)
  : resolve(__dirname, `../codex/core/pixelbrain/scdl/fixtures/${opts.name}.scdl`);
writeFileSync(outPath, source, 'utf8');
console.log(`[slime-gen] wrote ${bodyCells.length} body cells + ${highlightCells.length} highlight cells to ${outPath}`);
console.log(`[slime-gen] palette: core=${hexCore} rim=${hexRim} glow=${hexGlow} shine=${hexShine} shadow=${hexShadow}`);

const tierCounts = bodyCells.reduce((acc, c) => {
  acc[c.color] = (acc[c.color] || 0) + 1;
  return acc;
}, {});
console.log(`[slime-gen] tier distribution: ${JSON.stringify(tierCounts)}`);
