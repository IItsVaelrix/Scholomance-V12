/**
 * Returns the stroke array for a given character, or `null` if unknown.
 * Lookup is case-sensitive; the component should fall back to a system-font
 * <text> render for missing glyphs so non-atlas characters still display.
 */
export function getGlyphStrokes(char) {
  if (!char) return null;
  return GLYPH_ATLAS[char] || null;
}

/**
 * Returns true when the character is whitespace and should render as an
 * empty cell of fixed advance width (no strokes, no fallback text).
 */
export function isWhitespaceGlyph(char) {
  return char === ' ' || char === '\t' || char === ' ';
}
