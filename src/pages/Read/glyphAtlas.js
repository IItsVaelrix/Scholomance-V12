/**
 * Procedural Glyph Atlas — Scholomance Universal Typeface
 *
 * Each glyph is a list of SVG path d-strings rendered into a 12×18 cell.
 * Cells are monospaced; advance width is constant regardless of glyph complexity.
 * Strokes use stroke-linecap:round and stroke-linejoin:round, so multi-stroke
 * letters feel like brush passes rather than mechanical line segments.
 *
 * Cell geometry (in SVG user units):
 *   width:        12
 *   height:       18
 *   cap-top:      y =  2
 *   x-height-top: y =  6
 *   baseline:     y = 14
 *   descender:    y = 17
 *   left margin:  x =  1.5
 *   right margin: x = 10.5
 *   center:       x =  6
 *
 * Design rules (so the typeface stays coherent as it grows):
 *   1. Stems are vertical at x = 2.5 (left) or x = 9.5 (right) for 9-unit width.
 *   2. Bowls overshoot the baseline/cap-top by ~0.5u for optical correction.
 *   3. Crossbars sit slightly above geometric center (eye reads center as low).
 *   4. Multi-stroke letters split where a calligrapher would lift the pen.
 *   5. Single-path letters use one continuous motion — joins are smoother.
 */

